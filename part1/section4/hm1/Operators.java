package part1.section4.hm1;

public class Operators {
	static int x = 5;
	static int y = 3;
	static int z = 8;
	static int t = 11;

	public static void main(String[] args) {
		System.out.println("--x  *  --x = " + --x * --x);
		System.out.println("++y  *  ++y = " + ++y * ++y);
		System.out.println("z--  *  z-- = " + z-- * z--);
		System.out.println("t++  *  t++ = " + t++ * t++);

	}

}
